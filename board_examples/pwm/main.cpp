/******************************************************************************\
		INCLUDES
\******************************************************************************/
#include <ClockInterface.hpp>
#include <PwmInterface.hpp>

/******************************************************************************\
		DEFINES
\******************************************************************************/
#define PWM_PORT							1
#define PWM_PIN								1

/******************************************************************************\
		GLOBALS
\******************************************************************************/
//initialize pwm with frequency of 100hz, duty can change from 0 to 100
::pwm::ConcretePwm pwm0(PWM_PORT, PWM_PIN, 100/*Hz*/, 100 /* From 0 to 100 */);

/******************************************************************************\
		FUNCTIONS
\******************************************************************************/
int main(void)
{
	while (1)
	{
		pwm0.setDuty(0); // 0%
		clock::msleep(1000);

		pwm0.setDuty(25); // 25%
		clock::msleep(1000);

		pwm0.setDuty(50); // 50%
		clock::msleep(1000);

		pwm0.setDuty(75); // 75%
		clock::msleep(1000);

		pwm0.setDuty(100); // 100%
		clock::msleep(1000);
	}
}

