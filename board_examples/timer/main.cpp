/******************************************************************************\
		INCLUDES
\******************************************************************************/
#include <hal/Gpio.hpp>
#include <hal/Clock.hpp>
#include <hal/Timer.hpp>
#include <Pinout.hpp>

/******************************************************************************\
		DEFINES
\******************************************************************************/

/******************************************************************************\
		PROTOTYPES
\******************************************************************************/
void Led_High_ISR();

/******************************************************************************\
		GLOBALS
\******************************************************************************/
//configure led
::pinout::ConcretePinout pinLed(LED0_PORT, LED0_PIN);

//configure timer
::timer::ConcreteTimer timer0(DEFAULT_TIMER, ::timer::SubdivisionDelayMs, 1000 /*Period*/, &Led_High_ISR);

/******************************************************************************\
		FUNCTIONS
\******************************************************************************/
//led interrupt service routine
void Led_High_ISR()
{
	pinLed.high();

	return;
}

//------------------------------------------------------------------------------
int main(void)
{

	//force led low
	pinLed.low();

	//enable timer
	timer0.enable();

	while (1)
	{
		::clock::msleep(300);
		pinLed.low();
	}

}

